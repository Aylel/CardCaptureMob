package com.aylel.cardcapturemob.cards;

public class PassiveBonus {
    public double speed = 0.0;
    public double hp = 0.0;
    public double mana = 0.0;

    public PassiveBonus() {}

    public PassiveBonus(double speed, double hp, double mana) {
        this.speed = speed;
        this.hp = hp;
        this.mana = mana;
    }
}

